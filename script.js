let firstNumber = prompt("Введіть перше число:");
let secondNumber = prompt("Введіть друге число число:");

let sum = Number(firstNumber) + Number(secondNumber);
let diff = Number(firstNumber) - Number(secondNumber);
let prod = Number(firstNumber) * Number(secondNumber);
let frac = Number(firstNumber) / Number(secondNumber);

alert("Сумма двух чисел: " + sum + 
"\nРізниця двух чисел: " + diff + 
"\nДобуток двух чисел: " + prod + 
"\nЧастка двух чисел: " + frac);

